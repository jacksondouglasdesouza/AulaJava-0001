package aulaJava_0001;

//Criar uma calculadora para diferenciar duas idade.

public class CalculadoraDiferencaIdade {
	public static void main(String[] args) {
		int primeiraIdade = 30;
		int segundaIdade = 28;
		int diferencaIdade = primeiraIdade - segundaIdade;

		System.out.println("A diferença de idade é:  " + diferencaIdade);
	}

}
